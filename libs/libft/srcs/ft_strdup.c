#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*t;

	i = ft_strlen((char *)s);
	t = malloc(i + 1);
	if (!t)
		return (NULL);
	i = 0;
	while (s[i])
	{
		t[i] = s[i];
		i++;
	}
	t[i] = '\0';
	return (t);
}
